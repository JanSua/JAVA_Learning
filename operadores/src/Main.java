public class Main {
    public static void main(String[] args) {
        int numero1 = 5;
        numero1 += 1; // numero1 = numero1 + 1
        System.out.println("Numero + 1: " + numero1);

        numero1 -= 1; // numero1 = numero1 - 1
        System.out.println("Numero - 1: " + numero1);

        numero1 *= 2;
        System.out.println("Numero * 2: " + numero1);

        numero1 /= 3;
        System.out.println("Numero div 3: " + numero1);
    }
}